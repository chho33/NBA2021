	function fetchGames() {
		fetch("https://api.the-odds-api.com/v3/odds?api_key=8699dfb86a358ebfc4983fa7e23661ef&sport=basketball_nba&region=us")
			.then(response => response.json())
			.then((data) => {
				const oddListElement = document.getElementById('oddList');
				oddListElement.innerHTML = '';
				var tmp = [];
				var games = data.data;
				for (let index in games) {
					var game = games[index];
					tmp.push(createTable(game.sites, game.commence_time, game.home_team, game.teams, index));
				}
				oddListElement.innerHTML = tmp;
			});
		
	}
	
	function createTable(sites, commence_time, home_team, teams, index) {
		const removeArrayItem = (arr, itemToRemove) => {
		  return arr.filter(item => item !== itemToRemove)
		}
		var guest_team = removeArrayItem(teams, home_team)[0];
		var datetime = new Date(commence_time * 1000);
		dateString = datetime.toGMTString();
		var baseHTML = `<table class="table table-sm table-dark"><thead><tr><th scope="col">Site</th><th scope"col">${dateString} ${home_team} vs ${guest_team}</th></tr></thead><tbody>`
		for (var site of sites) {
			var site_key = site.site_key;
			var odd = site.odds.h2h;
			baseHTML += `<tr><th scope="row">${site_key}</th><td>${odd[0]} : ${odd[1]}</td>`
		}
		return baseHTML + '</tbody></table>';
	}
	
